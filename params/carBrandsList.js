export function getCarBrandsIcon(data) {
  if (data) {
    let selectedBrand = carBrandsList.find(function (item) {
      return item.value.toLowerCase() === data.toLowerCase();
    })

    if (selectedBrand) {
      return selectedBrand.icon;
    } else {
      return null;
    }
  } else {
    return null;
  }
}

export function getCarBrand(data) {
  if (data) {
    let selectedBrand = carBrandsList.find(function (item) {
      return item.value.toLowerCase() === data.toLowerCase();
    })

    if (selectedBrand) {
      return selectedBrand;
    } else {
      return {
        value: null,
        icon : null
      };
    }
  } else {
    return  {
      value: null,
      icon : null
    };
  }
}

