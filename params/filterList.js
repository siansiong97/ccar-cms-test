export function getFilterIcon(filter) {
    if (filter) {
        let selectedFilter = FilterList.find(function (item) {
            return item.value == filter;
        })

        if (selectedFilter) {
            return selectedFilter.icon;
        } else {
            return null;
        }
    } else {
        return null;
    }
}

export function getFilterText(filter) {
    if (filter) {
        let selectedFilter = FilterList.find(function (item) {
            return item.value == filter;
        })

        if (selectedFilter) {
            return selectedFilter.text;
        } else {
            return null;
        }
    } else {
        return null;
    }
}
